class DPOExample(BaseModel):
    """
    One Direct Preference Optimization training pair.

    TRL's DPOTrainer consumes this format directly (prompt, chosen, rejected).

    The schema extends TRL's minimal requirement with alignment research fields:
    - preference_strength: enables weighted DPO variants
    - rejection_strategy:  enables ablation on rejection diversity
    - alignment_signal:    captures reward / safety scores if available

    Fields:
        pair_id:            Unique identifier. Traceable back to sft_example_id.
        sft_example_id:     The SFTExample this pair was derived from.
        prompt:             Chat-template-formatted prompt string with generation
                            prefix applied (add_generation_prompt=True).
                            This is what DPOTrainer uses to locate response boundaries.
        chosen:             The preferred response string (no special tokens).
        rejected:           The dispreferred response string (no special tokens).
        preference_strength: Float [0, 1]. How confident we are in the preference.
                            0.5 = indeterminate, 1.0 = absolute.
                            Standard DPO treats all pairs as 1.0.
                            Weighted DPO uses this to scale the loss contribution.
        rejection_strategy: How the rejected response was generated.
        task_type:          Task routing key.
        difficulty:         Difficulty tier of the source example.
        created_at:         Generation timestamp.
    """
    pair_id: str = Field(default_factory=lambda: str(uuid4()))
    sft_example_id: Optional[str] = None
    prompt: str = Field(..., min_length=1)
    chosen: str = Field(..., min_length=1)
    rejected: str = Field(..., min_length=1)
    preference_strength: float = Field(
        default=1.0,
        ge=0.0,
        le=1.0,
        description="Preference confidence: 0.5=indeterminate, 1.0=absolute",
    )
    rejection_strategy: RejectionStrategy = RejectionStrategy.HALLUCINATED_FIELDS
    task_type: TaskType = TaskType.INSTRUCTION_FOLLOWING
    difficulty: DifficultyLevel = DifficultyLevel.MEDIUM
    created_at: datetime = Field(default_factory=datetime.utcnow)

    @model_validator(mode="after")
    def chosen_differs_from_rejected(self) -> "DPOExample":
        if self.chosen.strip() == self.rejected.strip():
            raise ValueError(
                f"DPO pair {self.pair_id}: chosen and rejected are identical. "
                "This pair provides no learning signal."
            )
        return self

    def to_trl_dict(self) -> dict[str, str]:
        """
        Return the minimal dict format consumed by TRL's DPOTrainer.
        TRL requires exactly these three keys.
        """
        return {
            "prompt": self.prompt,
            "chosen": self.chosen,
            "rejected": self.rejected,
        }

    def to_dict(self) -> dict:
        return {
            "pair_id": self.pair_id,
            "sft_example_id": self.sft_example_id,
            "prompt": self.prompt,
            "chosen": self.chosen,
            "rejected": self.rejected,
            "preference_strength": self.preference_strength,
            "rejection_strategy": self.rejection_strategy.value,
            "task_type": self.task_type.value,
            "difficulty": self.difficulty.value,
            "created_at": self.created_at.isoformat(),
        }


