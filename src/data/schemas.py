"""
Pydantic schemas for the LLM Alignment System pipeline.

These schemas are the data contracts between every stage of the pipeline:

    DatasetOrchestrator → SFTExample, DPOExample
    SFTTrainer          ← SFTExample (via preprocessor)
    DPOTrainer          ← DPOExample
    EvaluationPipeline  → ModelPrediction, EvaluationResult
    Comparator          ← BenchmarkResult
    ModelRegistry       ← ModelCheckpointMetadata
    WandB               ← ExperimentConfig

Design principles:
    1. Every field has a type. No bare `dict` or `Any` fields.
    2. Optional fields use `Optional[T] = None`, not missing keys.
    3. Enums for all categorical values — prevents silent string typos.
    4. Validators enforce invariants that cannot be expressed by types alone.
    5. Every schema has a `to_dict()` convenience method for JSON serialisation
       that handles non-serialisable types (datetime, enum, Path).

Local / cloud portability:
    All schemas are pure Python dataclass-equivalents. No GPU, no CUDA,
    no model loading. Import and use anywhere in the pipeline.
"""
from __future__ import annotations

import json
from datetime import datetime
from enum import Enum
from pathlib import Path
from typing import Any, Literal, Optional
from uuid import uuid4

from pydantic import BaseModel, Field, field_validator, model_validator


# ─────────────────────────────────────────────────────────────────────────────
# Enumerations
# Using Enum rather than Literal for values that appear in multiple schemas.
# This ensures the string "sft" in SFTExample and "sft" in ExperimentConfig
# are the same value, not two independently-typed string literals.
# ─────────────────────────────────────────────────────────────────────────────

class Role(str, Enum):
    """Valid roles in a conversational message turn."""
    SYSTEM = "system"
    USER = "user"
    ASSISTANT = "assistant"
    TOOL = "tool"


class ConversationQuality(str, Enum):
    """
    Provenance classification for SFT training examples.

    Controls training data weighting and quality-stratified ablations.
    HUMAN_WRITTEN > SYNTHETIC_LLM > SYNTHETIC_TEMPLATE in quality order.
    """
    HUMAN_WRITTEN = "human_written"           # Human-authored ground truth
    SYNTHETIC_LLM = "synthetic_llm"           # Generated by a stronger LLM
    SYNTHETIC_TEMPLATE = "synthetic_template" # Generated by code templates


class TaskType(str, Enum):
    """
    Task taxonomy for the alignment system.

    Used to route examples to the correct evaluator and to compute
    per-task breakdown statistics in benchmark results.
    """
    INSTRUCTION_FOLLOWING = "instruction_following"
    STRUCTURED_EXTRACTION = "structured_extraction"
    TOOL_CALLING = "tool_calling"
    SUMMARISATION = "summarisation"
    QUESTION_ANSWERING = "question_answering"
    REASONING = "reasoning"
    ALIGNMENT_EVAL = "alignment_eval"         # For red-teaming / safety evals
    OPEN_ENDED = "open_ended"


class DifficultyLevel(str, Enum):
    """
    Difficulty classification used for stratified evaluation reporting.
    adversarial examples stress model robustness.
    """
    EASY = "easy"
    MEDIUM = "medium"
    HARD = "hard"
    ADVERSARIAL = "adversarial"


class RejectionStrategy(str, Enum):
    """
    How a DPO rejected response was generated.

    Tracking this enables ablation studies on rejection strategy diversity:
    - Does mixing strategies outperform a single strategy?
    - Which strategy provides the most alignment signal?
    - Are certain failure modes more common in production?
    """
    HALLUCINATED_FIELDS = "hallucinated_fields"   # Added facts not in source
    SCHEMA_VIOLATION = "schema_violation"         # Wrong types / missing keys
    TRUNCATED = "truncated"                       # Cut off before completion
    VERBOSE_WRAPPER = "verbose_wrapper"           # Correct content, wrong format
    WRONG_NULLS = "wrong_nulls"                   # Over-conservative extraction
    INCORRECT_TOOL = "incorrect_tool"             # Wrong tool selected
    UNSAFE_CONTENT = "unsafe_content"             # Harmful or biased output
    FACTUAL_ERROR = "factual_error"               # Incorrect factual claims
    INSTRUCTION_IGNORED = "instruction_ignored"   # Ignored system prompt
    HUMAN_REJECTED = "human_rejected"             # Human annotator preference


class FailureMode(str, Enum):
    """
    Categorical error taxonomy for evaluation result analysis.
    Multiple failure modes can apply to a single prediction.
    """
    FORMAT_ERROR = "format_error"             # Wrong output format
    FACTUAL_HALLUCINATION = "factual_hallucination"
    FIELD_HALLUCINATION = "field_hallucination"  # Extra schema fields invented
    SCHEMA_VIOLATION = "schema_violation"
    INSTRUCTION_IGNORED = "instruction_ignored"
    TRUNCATED_OUTPUT = "truncated_output"
    REFUSAL = "refusal"                       # Model refused to answer
    OFF_TOPIC = "off_topic"
    UNSAFE_OUTPUT = "unsafe_output"
    REPETITION = "repetition"
    NONE = "none"                             # No failure — used for clean passes


class ModelStage(str, Enum):
    """
    Which stage of the alignment pipeline produced a prediction.
    Used to label BenchmarkResult rows for the final comparison table.
    """
    BASE = "base"
    PROMPT_ENGINEERED = "prompt_engineered"
    SFT = "sft"
    DPO = "dpo"
    RLHF = "rlhf"


