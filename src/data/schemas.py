class AlignmentSignal(BaseModel):
    """
    Structured alignment quality signals for a single response.

    Used in evaluation and optionally in weighted DPO.
    Scores are floats in [0, 1] — 1.0 is optimal, 0.0 is worst.

    Fields:
        reward_score:       Scalar reward from a reward model or heuristic.
                            None if no reward model was used.
        safety_score:       Probability the response is safe/harmless.
        helpfulness_score:  Subjective helpfulness rating.
        honesty_score:      Whether claims are grounded and accurate.
        instruction_score:  Whether the response followed the instruction.
        is_harmful:         Hard flag — True triggers rejection regardless of scores.
        harm_categories:    Specific harm types detected if any.
    """
    reward_score: Optional[float] = Field(default=None, ge=0.0, le=1.0)
    safety_score: float = Field(default=1.0, ge=0.0, le=1.0)
    helpfulness_score: float = Field(default=0.5, ge=0.0, le=1.0)
    honesty_score: float = Field(default=0.5, ge=0.0, le=1.0)
    instruction_score: float = Field(default=0.5, ge=0.0, le=1.0)
    is_harmful: bool = False
    harm_categories: list[str] = Field(default_factory=list)

    @property
    def composite_score(self) -> float:
        """
        Weighted composite of alignment dimensions.
        Safety is weighted highest because harm is non-negotiable.
        """
        return round(
            0.35 * self.safety_score
            + 0.25 * self.helpfulness_score
            + 0.25 * self.honesty_score
            + 0.15 * self.instruction_score,
            4,
        )


class EvaluationResult(BaseModel):
    """
    Evaluation metrics for one ModelPrediction against a reference.

    This is the output of one call to MetricsComputer.evaluate().
    Stored per-example so you can drill into individual failures.

    Fields:
        result_id:              Unique identifier.
        prediction_id:          Links back to the ModelPrediction.
        example_id:             Links back to the SFTExample.
        model_stage:            Which pipeline stage was evaluated.
        task_type:              Task routing key.
        difficulty:             Difficulty tier.
        exact_match:            Boolean — prediction == reference (normalised).
        format_valid:           Boolean — output is valid JSON / follows format.
        schema_compliant:       Boolean — output matches target Pydantic schema.
        instruction_followed:   Boolean — response respected the system prompt.
        hallucination_detected: Boolean — invented fields / facts detected.
        bleu:                   BLEU score against reference [0, 1].
        rouge_l:                ROUGE-L F1 against reference [0, 1].
        field_f1:               Token-level F1 across structured fields [0, 1].
        latency_ms:             Inference latency from the ModelPrediction.
        tokens_per_second:      Throughput from the ModelPrediction.
        failure_modes:          Categorical error labels for this example.
        alignment_signal:       Optional alignment quality scores.
    """
    result_id: str = Field(default_factory=lambda: str(uuid4()))
    prediction_id: Optional[str] = None
    example_id: Optional[str] = None
    model_stage: ModelStage
    task_type: TaskType
    difficulty: DifficultyLevel

    # Core alignment metrics
    exact_match: bool = False
    format_valid: bool = False
    schema_compliant: bool = False
    instruction_followed: bool = False
    hallucination_detected: bool = False

    # Lexical similarity metrics
    bleu: float = Field(default=0.0, ge=0.0, le=1.0)
    rouge_l: float = Field(default=0.0, ge=0.0, le=1.0)
    field_f1: float = Field(default=0.0, ge=0.0, le=1.0)

    # Latency (copied from prediction for fast aggregation)
    latency_ms: float = Field(default=0.0, ge=0.0)
    tokens_per_second: float = Field(default=0.0, ge=0.0)

    # Failure analysis
    failure_modes: list[FailureMode] = Field(default_factory=list)
    failure_notes: Optional[str] = None

    # Alignment signal
    alignment_signal: Optional[AlignmentSignal] = None

    created_at: datetime = Field(default_factory=datetime.utcnow)

    @property
    def passed(self) -> bool:
        """True if the prediction meets the minimum acceptance bar."""
        return self.format_valid and not self.hallucination_detected

    @property
    def alignment_score(self) -> float:
        """
        Composite alignment score for ranking purposes.
        Weights instruction following and hallucination avoidance highest.
        """
        return round(
            0.25 * float(self.format_valid)
            + 0.25 * float(self.instruction_followed)
            + 0.20 * float(not self.hallucination_detected)
            + 0.15 * self.field_f1
            + 0.10 * self.rouge_l
            + 0.05 * float(self.exact_match),
            4,
        )

    def to_dict(self) -> dict:
        return {
            "result_id": self.result_id,
            "prediction_id": self.prediction_id,
            "example_id": self.example_id,
            "model_stage": self.model_stage.value,
            "task_type": self.task_type.value,
            "difficulty": self.difficulty.value,
            "exact_match": self.exact_match,
            "format_valid": self.format_valid,
            "schema_compliant": self.schema_compliant,
            "instruction_followed": self.instruction_followed,
            "hallucination_detected": self.hallucination_detected,
            "bleu": self.bleu,
            "rouge_l": self.rouge_l,
            "field_f1": self.field_f1,
            "latency_ms": self.latency_ms,
            "tokens_per_second": self.tokens_per_second,
            "alignment_score": self.alignment_score,
            "failure_modes": [f.value for f in self.failure_modes],
            "failure_notes": self.failure_notes,
            "passed": self.passed,
            "created_at": self.created_at.isoformat(),
        }


