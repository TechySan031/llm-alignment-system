class BenchmarkResult(BaseModel):
    """
    Aggregated evaluation metrics for one complete benchmark run.

    This is the primary output of EvaluationPipeline.run() and the
    data structure stored in metrics/sft_metrics.json, metrics/dpo_metrics.json.

    One BenchmarkResult per model stage per evaluation run.
    ModelComparator reads multiple BenchmarkResults to produce the
    final comparison table (Base → Prompt → SFT → DPO).

    Fields:
        run_id:                 Unique identifier for this benchmark run.
        model_id:               Model name or checkpoint path.
        model_stage:            Pipeline stage label.
        n_examples:             Total examples evaluated.
        n_tasks:                Number of distinct tasks evaluated.
        exact_match:            Fraction where prediction == reference.
        format_valid:           Fraction with valid output format.
        schema_compliant:       Fraction passing schema validation.
        instruction_followed:   Fraction following the system prompt.
        hallucination_rate:     Fraction with hallucinated content.
        avg_bleu:               Mean BLEU across all examples.
        avg_rouge_l:            Mean ROUGE-L F1 across all examples.
        avg_field_f1:           Mean token-level field F1.
        avg_alignment_score:    Mean composite alignment score.
        avg_latency_ms:         Mean inference latency.
        p95_latency_ms:         95th percentile inference latency.
        avg_tokens_per_second:  Mean throughput.
        by_task:                Per-task breakdown.
        by_difficulty:          Per-difficulty breakdown.
        failure_mode_counts:    Counts of each failure mode category.
        evaluated_at:           Timestamp.
    """
    run_id: str = Field(default_factory=lambda: str(uuid4()))
    model_id: str
    model_stage: ModelStage
    n_examples: int = 0
    n_tasks: int = 0

    # Global metrics (all tasks, all difficulties)
    exact_match: float = Field(default=0.0, ge=0.0, le=1.0)
    format_valid: float = Field(default=0.0, ge=0.0, le=1.0)
    schema_compliant: float = Field(default=0.0, ge=0.0, le=1.0)
    instruction_followed: float = Field(default=0.0, ge=0.0, le=1.0)
    hallucination_rate: float = Field(default=0.0, ge=0.0, le=1.0)
    avg_bleu: float = Field(default=0.0, ge=0.0, le=1.0)
    avg_rouge_l: float = Field(default=0.0, ge=0.0, le=1.0)
    avg_field_f1: float = Field(default=0.0, ge=0.0, le=1.0)
    avg_alignment_score: float = Field(default=0.0, ge=0.0, le=1.0)
    avg_latency_ms: float = Field(default=0.0, ge=0.0)
    p95_latency_ms: float = Field(default=0.0, ge=0.0)
    avg_tokens_per_second: float = Field(default=0.0, ge=0.0)

    # Stratified breakdowns
    by_task: dict[str, TaskMetrics] = Field(default_factory=dict)
    by_difficulty: dict[str, DifficultyMetrics] = Field(default_factory=dict)
    failure_mode_counts: dict[str, int] = Field(default_factory=dict)

    evaluated_at: datetime = Field(default_factory=datetime.utcnow)

    def summary(self) -> str:
        """
        Human-readable one-line summary for logging.
        Suitable for printing at the end of a training or evaluation run.
        """
        return (
            f"[{self.model_stage.value.upper()}] "
            f"format={self.format_valid * 100:.1f}% | "
            f"instr={self.instruction_followed * 100:.1f}% | "
            f"halluc={self.hallucination_rate * 100:.1f}% | "
            f"align={self.avg_alignment_score * 100:.1f}% | "
            f"lat={self.avg_latency_ms:.0f}ms | "
            f"n={self.n_examples}"
        )

    def to_dict(self) -> dict:
        d = self.model_dump()
        d["model_stage"] = self.model_stage.value
        d["by_task"] = {
            k: v.model_dump(mode="json") for k, v in self.by_task.items()
        }
        d["by_difficulty"] = {
            k: v.model_dump(mode="json") for k, v in self.by_difficulty.items()
        }
        d["evaluated_at"] = self.evaluated_at.isoformat()
        return d

    def delta_vs(self, baseline: "BenchmarkResult") -> dict[str, float]:
        """
        Compute percentage-point improvement over a baseline BenchmarkResult.
        Positive = improvement. Used in the final comparison table.
        """
        metrics = [
            "format_valid", "schema_compliant", "instruction_followed",
            "avg_alignment_score", "avg_field_f1", "avg_rouge_l",
        ]
        deltas: dict[str, float] = {}
        for m in metrics:
            self_val = getattr(self, m, 0.0)
            base_val = getattr(baseline, m, 0.0)
            deltas[f"{m}_delta_pp"] = round((self_val - base_val) * 100, 2)

        # For hallucination and latency, improvement means reduction
        deltas["hallucination_rate_delta_pp"] = round(
            (baseline.hallucination_rate - self.hallucination_rate) * 100, 2
        )
        deltas["latency_delta_ms"] = round(
            baseline.avg_latency_ms - self.avg_latency_ms, 1
        )
        return deltas


