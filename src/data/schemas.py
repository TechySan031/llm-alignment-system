class SFTExample(BaseModel):
    """
    One supervised fine-tuning training example.

    This is the primary data structure produced by DatasetOrchestrator
    and consumed by build_sft_datasets() → SFTDataCollator → SFTTrainer.

    Fields:
        example_id:    Unique identifier. Generated automatically if not set.
                       Used to join training examples with evaluation results.
        conversation:  The full conversation including the reference response.
        task_type:     Routing key for task-specific evaluation.
        difficulty:    Controls evaluation stratification.
        quality:       Provenance signal for training data weighting.
        source:        Free-form source description (e.g. "generated_invoice_v2").
        weight:        Sampling weight during training. Defaults to 1.0.
                       Set > 1.0 for HUMAN_WRITTEN examples, < 1.0 for
                       low-quality synthetic examples.
        created_at:    Generation timestamp for dataset versioning.
        tags:          Arbitrary labels for filtering (e.g. ["financial", "noisy"]).

    Properties:
        input_text:    The user message content (last user turn).
        target_text:   The reference assistant response (final turn content).
        system_prompt: The system prompt string.
    """
    example_id: str = Field(default_factory=lambda: str(uuid4()))
    conversation: Conversation
    task_type: TaskType
    difficulty: DifficultyLevel = DifficultyLevel.MEDIUM
    quality: ConversationQuality = ConversationQuality.SYNTHETIC_TEMPLATE
    source: str = Field(default="", description="Generator or dataset source identifier")
    weight: float = Field(default=1.0, ge=0.0, le=10.0)
    created_at: datetime = Field(default_factory=datetime.utcnow)
    tags: list[str] = Field(default_factory=list)

    @property
    def system_prompt(self) -> str:
        return self.conversation.system_prompt

    @property
    def input_text(self) -> str:
        """
        Return the user-facing input text (last user turn).
        For multi-turn conversations, this is the final user message.
        """
        user_turns = [t for t in self.conversation.turns if t.role == Role.USER]
        if not user_turns:
            raise ValueError(f"SFTExample {self.example_id} has no user turns")
        return user_turns[-1].content

    @property
    def target_text(self) -> str:
        """
        Return the reference response (final assistant turn).
        This is the text the model must learn to produce.
        Raises if the conversation has no assistant turn.
        """
        response = self.conversation.final_response
        if response is None:
            raise ValueError(
                f"SFTExample {self.example_id} has no final assistant turn"
            )
        return response

    def to_dict(self) -> dict:
        return {
            "example_id": self.example_id,
            "turns": [
                {"role": t.role.value, "content": t.content}
                for t in self.conversation.turns
            ],
            "task_type": self.task_type.value,
            "difficulty": self.difficulty.value,
            "quality": self.quality.value,
            "source": self.source,
            "weight": self.weight,
            "created_at": self.created_at.isoformat(),
            "tags": self.tags,
        }

    @classmethod
    def from_dict(cls, data: dict) -> "SFTExample":
        """Deserialise from a dict (loaded from JSONL disk storage)."""
        turns = [
            MessageTurn(role=Role(t["role"]), content=t["content"], turn_idx=i)
            for i, t in enumerate(data["turns"])
        ]
        return cls(
            example_id=data.get("example_id", str(uuid4())),
            conversation=Conversation(turns=turns),
            task_type=TaskType(data["task_type"]),
            difficulty=DifficultyLevel(data.get("difficulty", "medium")),
            quality=ConversationQuality(
                data.get("quality", "synthetic_template")
            ),
            source=data.get("source", ""),
            weight=float(data.get("weight", 1.0)),
            tags=data.get("tags", []),
        )


