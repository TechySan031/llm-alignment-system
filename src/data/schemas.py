# Core message and conversation primitives
# ─────────────────────────────────────────────────────────────────────────────

class MessageTurn(BaseModel):
    """
    A single turn in a multi-turn conversation.

    The atomic unit reused across SFTExample, DPOExample, and ModelPrediction.
    Keeping this as a separate model means chat format is defined once.

    Fields:
        role:      Who produced this message (system / user / assistant / tool).
        content:   The message text.
        turn_idx:  0-based position in the conversation (auto-assigned).
        metadata:  Optional unstructured metadata (tool call results, etc.).
    """
    role: Role
    content: str = Field(..., min_length=1)
    turn_idx: int = Field(default=0, ge=0)
    metadata: Optional[dict[str, Any]] = None

    @field_validator("content")
    @classmethod
    def content_not_whitespace_only(cls, v: str) -> str:
        if not v.strip():
            raise ValueError("Message content cannot be whitespace only")
        return v


class Conversation(BaseModel):
    """
    An ordered sequence of MessageTurns forming a complete conversation.

    Validates that:
        - First turn is always system (alignment training convention).
        - Turns alternate user/assistant after the system prompt.
        - At least one user and one assistant turn are present.

    The `prompt_only` property returns all turns except the final assistant
    response — this is the prefix used for DPO and generation.
    """
    turns: list[MessageTurn] = Field(..., min_length=2)

    @model_validator(mode="after")
    def validate_turn_structure(self) -> "Conversation":
        roles = [t.role for t in self.turns]

        if roles[0] != Role.SYSTEM:
            raise ValueError(
                f"First turn must be 'system', got '{roles[0]}'"
            )
        if Role.USER not in roles:
            raise ValueError("Conversation must contain at least one user turn")
        if Role.ASSISTANT not in roles:
            raise ValueError(
                "Conversation must contain at least one assistant turn"
            )
        return self

    @property
    def system_prompt(self) -> str:
        """Return the system prompt text."""
        return self.turns[0].content

    @property
    def prompt_only(self) -> list[MessageTurn]:
        """Return all turns except the final assistant response."""
        if self.turns[-1].role == Role.ASSISTANT:
            return self.turns[:-1]
        return self.turns

    @property
    def final_response(self) -> Optional[str]:
        """Return the final assistant turn content, or None."""
        if self.turns[-1].role == Role.ASSISTANT:
            return self.turns[-1].content
        return None

    @property
    def num_user_turns(self) -> int:
        return sum(1 for t in self.turns if t.role == Role.USER)


