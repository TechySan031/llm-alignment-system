class GenerationConfig(BaseModel):
    """
    The decoding configuration used for a specific inference call.
    Stored alongside predictions so results are fully reproducible.
    """
    max_new_tokens: int = Field(default=512, ge=1)
    temperature: float = Field(default=0.0, ge=0.0, le=2.0)
    do_sample: bool = False
    top_p: Optional[float] = Field(default=None, ge=0.0, le=1.0)
    top_k: Optional[int] = Field(default=None, ge=1)
    repetition_penalty: float = Field(default=1.1, ge=1.0)
    num_beams: int = Field(default=1, ge=1)


class ModelPrediction(BaseModel):
    """
    The output of one model inference call.

    Stores everything needed to:
    1. Evaluate the prediction (generated_text + prompt + reference).
    2. Reproduce the inference (generation_config + model_stage + model_id).
    3. Diagnose issues (latency_ms + input_tokens + output_tokens).
    4. Re-evaluate with new metrics (predictions saved separately from results).

    Fields:
        prediction_id:     Unique identifier.
        example_id:        Links back to the SFTExample that produced this.
        model_id:          Model name or checkpoint path.
        model_stage:       Which pipeline stage generated this.
        prompt:            The full formatted prompt sent to the model.
        generated_text:    Raw text output from the model.
        reference_text:    Ground truth answer (for metric computation).
        task_type:         Task routing key.
        difficulty:        Difficulty tier.
        input_tokens:      Number of prompt tokens.
        output_tokens:     Number of generated tokens.
        latency_ms:        Wall-clock inference time in milliseconds.
        generation_config: Decoding parameters used.
        log_probs:         Optional per-token log probabilities.
                           Used for perplexity and reward signal computation.
        created_at:        Inference timestamp.
    """
    prediction_id: str = Field(default_factory=lambda: str(uuid4()))
    example_id: Optional[str] = None
    model_id: str = Field(..., min_length=1)
    model_stage: ModelStage
    prompt: str
    generated_text: str = Field(default="")
    reference_text: Optional[str] = None
    task_type: TaskType = TaskType.INSTRUCTION_FOLLOWING
    difficulty: DifficultyLevel = DifficultyLevel.MEDIUM
    input_tokens: int = Field(default=0, ge=0)
    output_tokens: int = Field(default=0, ge=0)
    latency_ms: float = Field(default=0.0, ge=0.0)
    generation_config: GenerationConfig = Field(default_factory=GenerationConfig)
    log_probs: Optional[list[float]] = None
    created_at: datetime = Field(default_factory=datetime.utcnow)

    @property
    def tokens_per_second(self) -> float:
        if self.latency_ms <= 0:
            return 0.0
        total = self.input_tokens + self.output_tokens
        return round(total / (self.latency_ms / 1000), 1)

    @property
    def is_empty(self) -> bool:
        return len(self.generated_text.strip()) == 0

    def to_dict(self) -> dict:
        return {
            "prediction_id": self.prediction_id,
            "example_id": self.example_id,
            "model_id": self.model_id,
            "model_stage": self.model_stage.value,
            "prompt": self.prompt,
            "generated_text": self.generated_text,
            "reference_text": self.reference_text,
            "task_type": self.task_type.value,
            "difficulty": self.difficulty.value,
            "input_tokens": self.input_tokens,
            "output_tokens": self.output_tokens,
            "latency_ms": self.latency_ms,
            "tokens_per_second": self.tokens_per_second,
            "created_at": self.created_at.isoformat(),
        }


