class ExperimentConfig(BaseModel):
    """
    Complete configuration record for one training experiment.

    Logged to W&B at run start. Stored alongside checkpoint metadata.
    Enables reproducibility: given an ExperimentConfig, the full training
    run can be replicated from scratch.

    Fields grouped by concern:
        Identification:  experiment_id, run_name, description
        Model:           base_model_id, model_stage, lora_rank, lora_alpha,
                         target_modules, quantization_bits
        Training:        learning_rate, effective_batch_size, num_epochs,
                         warmup_ratio, weight_decay, lr_scheduler,
                         gradient_checkpointing, bf16
        Data:            dataset_id, dataset_version, train_size, max_seq_length
        Infrastructure:  device, gpu_name, num_gpus
    """
    experiment_id: str = Field(default_factory=lambda: str(uuid4()))
    run_name: str = Field(..., min_length=1)
    description: str = ""
    model_stage: ModelStage

    # Model config
    base_model_id: str
    lora_rank: Optional[int] = None
    lora_alpha: Optional[int] = None
    target_modules: list[str] = Field(default_factory=list)
    quantization_bits: Optional[int] = None

    # Training hyperparameters
    learning_rate: float = Field(..., gt=0)
    per_device_batch_size: int = Field(..., ge=1)
    gradient_accumulation_steps: int = Field(default=1, ge=1)
    num_epochs: float = Field(..., gt=0)
    warmup_ratio: float = Field(default=0.03, ge=0.0, le=1.0)
    weight_decay: float = Field(default=0.01, ge=0.0)
    lr_scheduler: str = "cosine"
    max_grad_norm: float = Field(default=1.0, gt=0)
    gradient_checkpointing: bool = True
    bf16: bool = True
    seed: int = 42

    # DPO-specific (None for SFT runs)
    dpo_beta: Optional[float] = Field(default=None, ge=0.0)
    dpo_loss_type: Optional[str] = None

    # Data
    dataset_id: Optional[str] = None
    dataset_version: str = "1.0.0"
    train_size: int = 0
    max_seq_length: int = 2048

    # Infrastructure
    device: str = "cuda"
    gpu_name: Optional[str] = None
    num_gpus: int = 1

    created_at: datetime = Field(default_factory=datetime.utcnow)

    @property
    def effective_batch_size(self) -> int:
        return self.per_device_batch_size * self.gradient_accumulation_steps * self.num_gpus

    def to_wandb_dict(self) -> dict[str, Any]:
        """
        Flatten config to a plain dict for W&B `wandb.init(config=...)`.
        W&B does not accept nested Pydantic models or datetime objects.
        """
        return {
            "experiment_id": self.experiment_id,
            "run_name": self.run_name,
            "model_stage": self.model_stage.value,
            "base_model_id": self.base_model_id,
            "lora_rank": self.lora_rank,
            "lora_alpha": self.lora_alpha,
            "lora_scale": (
                self.lora_alpha / self.lora_rank
                if self.lora_rank and self.lora_alpha else None
            ),
            "quantization_bits": self.quantization_bits,
            "learning_rate": self.learning_rate,
            "per_device_batch_size": self.per_device_batch_size,
            "gradient_accumulation_steps": self.gradient_accumulation_steps,
            "effective_batch_size": self.effective_batch_size,
            "num_epochs": self.num_epochs,
            "warmup_ratio": self.warmup_ratio,
            "weight_decay": self.weight_decay,
            "lr_scheduler": self.lr_scheduler,
            "max_grad_norm": self.max_grad_norm,
            "gradient_checkpointing": self.gradient_checkpointing,
            "bf16": self.bf16,
            "seed": self.seed,
            "dpo_beta": self.dpo_beta,
            "train_size": self.train_size,
            "max_seq_length": self.max_seq_length,
            "device": self.device,
            "gpu_name": self.gpu_name,
        }

    def to_dict(self) -> dict:
        d = self.to_wandb_dict()
        d["target_modules"] = self.target_modules
        d["dataset_id"] = self.dataset_id
        d["dataset_version"] = self.dataset_version
        d["created_at"] = self.created_at.isoformat()
        return d